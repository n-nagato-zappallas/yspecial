/**
 * 【数秘術】単数変換
 * 0:通常                 [9パターン]
 * 1:カバラ基本数         [11パターン]（1～9,11,22）
 * 2:フォーチュンサイクル [24パターン]
 */
function get_singular( $type, $num ) {
	$ret = $num;
	while( true ) {
		$tmp = 0;
		for( $i = 0; $i < strlen( $ret ); $i++ )
			$tmp += substr( $ret, $i, 1 );
		$ret = $tmp;
		if( $type == 2 ) {
			$ret = ( $ret -1 )%24 +1;
			break;
		} else {
			if( strlen( $ret ) == 1 )
				break;
			else if( $type && ( $ret == 11 || $ret == 22 ) ) {
				$ret = $ret == 11 ?10 :11;
				break;
			}
		}
	}
	return $ret;
}

/**
 * 【九星気学】値の正常化
 */
function get_kyusei( $num ) {
	$ret = $num;
	if( $ret > 9 ) $ret -=9;
	else if( $ret < 1 ) $ret +=9;
	if( $ret > 9 || $ret < 1 ) $ret = get_kyusei( $ret );
	return $ret;
}

